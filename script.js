function createTable(headers, rows) {
  let html = "<div class='table-responsive'>"; // contenedor para scroll
  html += "<table>";
  html += "<thead><tr>";
  headers.forEach(h => {
    html += `<th>${h}</th>`;
  });
  html += "</tr></thead><tbody>";
  rows.forEach(row => {
    html += "<tr>";
    for (const key in row) {
      html += `<td>${row[key]}</td>`;
    }
    html += "</tr>";
  });
  html += "</tbody></table>";
  html += "</div>";
  return html;
}

